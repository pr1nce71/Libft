#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	size_t	i;
	size_t	len1;
	size_t	len2;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	result = (char *)malloc(sizeof(char) * (len1 + len2 +1));
	if (!result)
		return (NULL);
	i = 0;
	while (*s1)
		*result++ = *s1++;
	while (*s2)
		*result++ = *s2++;
	result[i] = '\0';
	return (result);
}
